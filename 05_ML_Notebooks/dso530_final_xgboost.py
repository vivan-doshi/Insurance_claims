import numpy as np
import pandas as pd
from sklearn.model_selection import KFold, cross_val_score
from sklearn.metrics import mean_squared_error
from xgboost import XGBRegressor, XGBClassifier
import optuna

def train_zit_xgb(X, y, n_trials=50, random_state=42):
    """
    Train a zero‚Äëinflated Tweedie model (classifier √ó reg:tweedie severity)
    with Optuna tuning over learning‚Äërate and Tweedie power.

    Parameters
    ----------
    X : pandas DataFrame or 2‚ÄëD array
    y : pandas Series / 1‚ÄëD array (lc or halc)
    n_trials : Optuna trials to run
    random_state : for reproducibility

    Returns
    -------
    model_dict : {
        'clf'   : fitted XGBClassifier,
        'reg'   : fitted XGBRegressor,
        'study' : Optuna study object,
        'predict' : callable -> array of expected losses
    }
    """
    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # 0.  Clean rows with missing target
    mask = ~pd.isna(y)
    X_clean, y_clean = X[mask], y[mask]

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # 1.  Stage‚Äë1 classifier (zero vs non‚Äëzero)
    y_is_pos = (y_clean > 0).astype(int)
    clf = XGBClassifier(
        objective="binary:logistic",
        n_estimators=400,
        learning_rate=0.05,
        max_depth=4,
        subsample=0.8,
        colsample_bytree=0.8,
        scale_pos_weight=(len(y_clean) - y_is_pos.sum()) / y_is_pos.sum(),  # balance 0/1
        random_state=random_state,
        n_jobs=-1
    )
    clf.fit(X_clean, y_is_pos)

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # 2.  Prepare data for severity model (only positive rows)
    X_sev = X_clean[y_is_pos == 1]
    y_sev = y_clean[y_is_pos == 1]

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # 3.  Hyper‚Äëparameter tuning with Optuna
    kf = KFold(n_splits=5, shuffle=True, random_state=random_state)

    def objective(trial):
        p   = trial.suggest_float('p', 1.05, 1.95)
        eta = trial.suggest_float('eta', 0.01, 0.2, log=True)
        depth = trial.suggest_int('max_depth', 3, 8)
        subs  = trial.suggest_float('subsample', 0.6, 1.0)
        col   = trial.suggest_float('colsample_bytree', 0.6, 1.0)
        n_est = trial.suggest_int('n_estimators', 300, 2000)

        reg = XGBRegressor(
            objective="reg:tweedie",
            tweedie_variance_power=p,
            learning_rate=eta,
            max_depth=depth,
            subsample=subs,
            colsample_bytree=col,
            n_estimators=n_est,
            random_state=random_state,
            eval_metric="rmse",
            n_jobs=-1
        )

        cv_rmse = -cross_val_score(
            reg, X_sev, y_sev,
            cv=kf,
            scoring="neg_root_mean_squared_error",
            n_jobs=-1
        ).mean()

        return cv_rmse

    study = optuna.create_study(direction="minimize", sampler=optuna.samplers.TPESampler(seed=random_state))
    study.optimize(objective, n_trials=n_trials, show_progress_bar=True)

    best_params = study.best_params
    print(f"üèÜ best params  ‚Üí  power={best_params['p']:.3f} | eta={best_params['eta']:.3f} "
          f"| RMSE={study.best_value:,.2f}")

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # 4.  Fit severity model with best hyper‚Äëparameters on *all* positives
    reg = XGBRegressor(
        objective="reg:tweedie",
        tweedie_variance_power=best_params['p'],
        learning_rate=best_params['eta'],
        max_depth=best_params['max_depth'],
        subsample=best_params['subsample'],
        colsample_bytree=best_params['colsample_bytree'],
        n_estimators=best_params['n_estimators'],
        random_state=random_state,
        eval_metric="rmse",
        n_jobs=-1
    )
    reg.fit(X_sev, y_sev)

    # ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ‚îÄ
    # 5.  Unified prediction function:  E[Y] = P(Y>0) √ó E[Y|Y>0]
    def predict(x_new):
        p_claim  = clf.predict_proba(x_new)[:, 1]
        mean_sev = reg.predict(x_new)
        return p_claim * mean_sev

    return {
        "clf": clf,
        "reg": reg,
        "study": study,
        "predict": predict
    }

